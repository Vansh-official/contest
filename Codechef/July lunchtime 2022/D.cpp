using namespace std;

int n, m, i, j;

void solve(int T)
{
    cin >> n;
    vector<long long> a(n);
    for (i = 0; i < n; i++)
        cin >> a[i];
    long long sum = 0, ans = 0;
    sort(a.begin(), a.end());
    for (i = 0; i < n; i++)
        if (sum + a[i] < n)
        {
            sum += a[i];
            ans++;
        }
        else
            break;
    cout << ans;
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin >> t;

    for (int p = 1; p <= t; p++)
    {
        solve(p);
        cout << "\n";
    }
}