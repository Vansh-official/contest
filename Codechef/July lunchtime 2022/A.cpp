int n, m, i, j;

void solve(int T)
{
    string s;
    cin >> n >> s;
    int ct = 0;
    for (i = 0; i < n; i++)
    {
        if (s[i] != 'a' && s[i] != 'e' && s[i] != 'i' && s[i] != 'o' && s[i] != 'u')
        {
            ct = 1;
            while (i < n - 1 && (s[i + 1] != 'a' && s[i + 1] != 'e' && s[i + 1] != 'i' && s[i + 1] != 'o' && s[i + 1] != 'u'))
            {
                i++;
                ct++;
            }
            if (ct >= 4)
            {
                cout << "NO";
                return;
            }
        }
    }
    cout << "YES";
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin >> t;

    for (int p = 1; p <= t; p++)
    {
        solve(p);
        cout << "\n";
    }
}