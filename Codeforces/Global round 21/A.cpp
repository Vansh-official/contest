using namespace std;

int n, m, k, i, j, x;

void solve(int T)
{
    cin >> n >> m;
    vector<int> arr(n);
    int ans = 0;
    for (i = 0; i < n; i++)
    {
        cin >> arr[i];
        ans = max(ans, (arr[i] | m));
    }
    cout << ans;
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin >> t;

    for (int p = 1; p <= t; p++)
    {
        solve(p);
        cout << "\n";
    }
}