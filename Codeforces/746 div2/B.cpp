using namespace std;

void solve(int T)
{
    int n, x;
    cin >> n >> x;
    vector<pair<int, int>> arr(n);
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i].first;
        arr[i].second = i;
    }
    sort(arr.begin(), arr.end());
    bool ans = 1;
    for (int i = 0; i < n; i++)
    {
        if (arr[i].second == i)
        {
            continue;
        }
        ans = 0;
    }
    if (ans)
    {
        cout << "YES";
        return;
    }
    else
    {
        cout << "NO";
    }
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin >> t;

    for (int i = 1; i <= t; i++)
    {
        solve(t);
        cout << "\n";
    }
}